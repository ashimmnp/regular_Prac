let shiftToLeft = (a,b) => {
    p = a<<b;
    return p;
}
eg1 = shiftToLeft(5,2);
eg2 = shiftToLeft(10,3);
eg3 = shiftToLeft(-32,2);
eg4 = shiftToLeft(-6,5);
eg5 = shiftToLeft(12,4);
eg6 = shiftToLeft(46,6);
console.log("Eg1: ",eg1);
